num1=float(input("Please enter the first number : "))
num2=float(input("Please enter the second number : "))
op=(input("Please enter the operation : "))
if(op=='+'):
    print(num1+num2)
elif(op=='-'):
    print(num1-num2)    
elif(op=='*'):
    print(num1*num2)    
elif(op=='/'):
    print(num1/num2)  
elif(op=='//'):
    print(num1//num2)  
elif(op=='%'):
    print(num1%num2) 
else:
    print("The operation is wrong ","Please try again later!")           












